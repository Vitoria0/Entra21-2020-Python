#classe plastico
class plastico:
    def __init__(self, garrafa, marca, tipo):
        self.garrafa = garrafa
        self.marca = marca
        self.tipo = tipo

    def printar(self):
        return print(self.garrafa, self.marca, self.tipo)

a = plastico('Plástica,', 'Pure,', 'Sem gáz')

a.printar()

